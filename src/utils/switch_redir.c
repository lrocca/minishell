#include "main.h"

char	switch_redir(const char *str)
{
	if (str[0] == '<' && str[1] != '<')
		return (1);
	else if (str[0] == '>' && str[1] != '>')
		return (2);
	else if (str[0] == '<' && str[1] == '<')
		return (3);
	else if (str[0] == '>' && str[1] == '>')
		return (4);
	return (0);
}
